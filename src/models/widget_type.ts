/** An enum indicating the type of a widget. */
export enum WidgetType {
  /** The widget does not have a specific {@link IWidgetType} assigned. */
  Unknown = "Unknown",
  /** The widget is a text widget. */
  Text = "Text",
  /** The widget is an image widget. */
  Image = "Image",
}
